function enderecoModel(enderecoId, contatoId, cep, logradouro, numero, complemento, bairro, cidade, estado) {
    this.EnderecoId = enderecoId;
    this.ContatoId = contatoId;
    this.Cep = cep;
    this.Logradouro = logradouro;
    this.Numero = numero;
    this.Complemento = complemento;
    this.Bairro = bairro;
    this.Cidade = cidade;
    this.Estado = estado;
}